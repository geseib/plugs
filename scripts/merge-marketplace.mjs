#!/usr/bin/env node
// Merge a ready-made marketplace manifest into data.js.
//
// Companion to scan.mjs: where scan.mjs builds a marketplace by scanning a
// plugin REPO, this merges a hand/agent-authored manifest.json that is already
// in catalog shape: { marketplace:{name,addSource,description}, plugins:[...] }.
// The marketplace is replaced in place (by name) or appended, leaving all other
// marketplaces in data.js untouched.
//
// Usage:
//   node scripts/merge-marketplace.mjs <manifest.json> [--out data.js] [--mode skills]
//
import fs from "node:fs";
import vm from "node:vm";

const args = process.argv.slice(2);
if (!args.length || args.includes("-h") || args.includes("--help")) {
  console.log(
    "Usage: node scripts/merge-marketplace.mjs <manifest.json> [--out data.js] [--mode skills]"
  );
  process.exit(args.length ? 0 : 1);
}

const flagVal = (name, def) => {
  const i = args.indexOf(name);
  return i >= 0 && args[i + 1] ? args[i + 1] : def;
};
const manifestPath = args.find((a) => !a.startsWith("--"));
const outPath = flagVal("--out", "data.js");
const mode = flagVal("--mode", "skills");

const manifest = JSON.parse(fs.readFileSync(manifestPath, "utf8"));
if (!manifest.marketplace || !Array.isArray(manifest.plugins)) {
  console.error("manifest must be { marketplace:{...}, plugins:[...] }");
  process.exit(1);
}

// Strip non-catalog metadata (e.g. absolute source paths) so data.js stays clean.
const plugins = manifest.plugins.map(({ source, ...rest }) => rest);
const entry = {
  marketplace: manifest.marketplace,
  plugins,
  generatedAt: new Date().toISOString(),
  mode,
};

function readExistingMarketplaces(p) {
  if (!fs.existsSync(p)) return [];
  const sandbox = { window: {} };
  vm.createContext(sandbox);
  vm.runInContext(fs.readFileSync(p, "utf8"), sandbox);
  const cat = sandbox.window.PLUGIN_CATALOG;
  if (!cat) return [];
  if (Array.isArray(cat.marketplaces)) return cat.marketplaces;
  if (cat.marketplace && Array.isArray(cat.plugins)) {
    return [{ marketplace: cat.marketplace, plugins: cat.plugins, generatedAt: cat.generatedAt }];
  }
  return [];
}

const marketplaces = readExistingMarketplaces(outPath);
const idx = marketplaces.findIndex((m) => m.marketplace && m.marketplace.name === entry.marketplace.name);
if (idx >= 0) marketplaces[idx] = entry;
else marketplaces.push(entry);

const banner =
  `// Generated by scripts/scan.mjs — do not edit by hand.\n` +
  `// Multi-marketplace catalog. Each entry: { marketplace:{name,addSource,description}, plugins:[...], generatedAt, mode }\n`;
fs.writeFileSync(outPath, `${banner}window.PLUGIN_CATALOG = ${JSON.stringify({ marketplaces }, null, 2)};\n`);

console.log(
  `Merged "${entry.marketplace.name}" (${entry.plugins.length} plugins). Catalog now has ${marketplaces.length} marketplace(s).`
);
